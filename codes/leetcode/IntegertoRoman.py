class Solution:
    def intToRoman(self, num: int) -> str:
        
        keys = ["M", "CM", "D", "CD", "C", "XC", "L", "XL", "X", "IX", "V", "IV", "I"]
        values = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1]
        
        roman = ""
        for i, value in enumerate(values):
            roman += keys[i] * (num // value)
            num %= value
            if num == 0:
                break
            
        return roman
